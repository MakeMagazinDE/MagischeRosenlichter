/// Firmware version
#define VERSION ((uint8_t)0x01)
/// Firmware revision
#define REVISION ((uint8_t)0x01)
/// Firmware sub revision
#define SUBREVISION ((uint8_t)0x00)
/// Firmware version as unsigned interger value
#define VERSION_UINT32 ((VERSION << 16) | (REVISION << 8) | (SUBREVISION))

